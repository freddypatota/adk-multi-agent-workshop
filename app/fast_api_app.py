import os

import google.auth
from fastapi import FastAPI
from google.adk.cli.fast_api import get_fast_api_app

from .utils.logging_config import get_logger, setup_logging
from .utils.telemetry import setup_telemetry
from .utils.typing import Feedback

setup_logging()
setup_telemetry()
_, project_id = google.auth.default()

logger = get_logger(__name__)
allow_origins = (
    os.getenv("ALLOW_ORIGINS", "").split(",") if os.getenv("ALLOW_ORIGINS") else None
)

# Artifact bucket for ADK (created by Terraform, passed via env var)
logs_bucket_name = os.environ.get("LOGS_BUCKET_NAME")

AGENT_DIR = os.path.join(os.path.dirname(os.path.abspath(__file__)), "agents")
# In-memory session configuration - no persistent storage
session_service_uri = None

artifact_service_uri = f"gs://{logs_bucket_name}" if logs_bucket_name else None

app: FastAPI = get_fast_api_app(
    agents_dir=AGENT_DIR,
    web=True,
    artifact_service_uri=artifact_service_uri,
    use_local_storage=False,
    allow_origins=allow_origins,
    session_service_uri=session_service_uri,
    otel_to_cloud=True,
)
app.title = "loan-drawdown-agent-demo"
app.description = "API for interacting with the Agent loan-drawdown-agent-demo"


@app.post("/feedback")
def collect_feedback(feedback: Feedback) -> dict[str, str]:
    """Collect and log feedback.

    Args:
        feedback: The feedback data to log

    Returns:
        Success message
    """
    logger.info("Feedback received", extra={"feedback": feedback.model_dump()})
    return {"status": "success"}


# Main execution
if __name__ == "__main__":
    import uvicorn

    uvicorn.run(app, host="0.0.0.0", port=8080)
